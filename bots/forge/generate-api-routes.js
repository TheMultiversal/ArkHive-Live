// ── Master Generation ──────────────────────────────────────────

function generate() {
  const routes = [];

  // /api/search
  const searchDir = path.join(APP_DIR, 'api/search');
  ensureDir(searchDir);
  fs.writeFileSync(path.join(searchDir, 'route.ts'), generateSearchRoute(), 'utf-8');
  routes.push('/api/search');

  // /api/entities/[type]
  const entityListDir = path.join(APP_DIR, 'api/entities/[type]');
  ensureDir(entityListDir);
  fs.writeFileSync(path.join(entityListDir, 'route.ts'), generateEntityListRoute(), 'utf-8');
  routes.push('/api/entities/[type]');

  // /api/entities/[type]/[slug]
  const entityDetailDir = path.join(APP_DIR, 'api/entities/[type]/[slug]');
  ensureDir(entityDetailDir);
  fs.writeFileSync(path.join(entityDetailDir, 'route.ts'), generateEntityDetailRoute(), 'utf-8');
  routes.push('/api/entities/[type]/[slug]');

  // /api/stats
  const statsDir = path.join(APP_DIR, 'api/stats');
  ensureDir(statsDir);
  fs.writeFileSync(path.join(statsDir, 'route.ts'), generateStatsRoute(), 'utf-8');
  routes.push('/api/stats');

  // /api/health
  const healthDir = path.join(APP_DIR, 'api/health');
  ensureDir(healthDir);
  fs.writeFileSync(path.join(healthDir, 'route.ts'), generateHealthRoute(), 'utf-8');
  routes.push('/api/health');

  // /api/export/[format]
  const exportDir = path.join(APP_DIR, 'api/export/[format]');
  ensureDir(exportDir);
  fs.writeFileSync(path.join(exportDir, 'route.ts'), generateExportRoute(), 'utf-8');
  routes.push('/api/export/[format]');

  // /api/audit
  const auditDir = path.join(APP_DIR, 'api/audit');
  ensureDir(auditDir);
  fs.writeFileSync(path.join(auditDir, 'route.ts'), generateAuditRoute(), 'utf-8');
  routes.push('/api/audit');

  // /api/submit
  const submitDir = path.join(APP_DIR, 'api/submit');
  ensureDir(submitDir);
  fs.writeFileSync(path.join(submitDir, 'route.ts'), generateSubmitRoute(), 'utf-8');
  routes.push('/api/submit');

  // /api/network/[slug]
  const networkDir = path.join(APP_DIR, 'api/network/[slug]');
  ensureDir(networkDir);
  fs.writeFileSync(path.join(networkDir, 'route.ts'), generateNetworkRoute(), 'utf-8');
  routes.push('/api/network/[slug]');

  // /api/timeline
  const timelineDir = path.join(APP_DIR, 'api/timeline');
  ensureDir(timelineDir);
  fs.writeFileSync(path.join(timelineDir, 'route.ts'), generateTimelineRoute(), 'utf-8');
  routes.push('/api/timeline');

  console.log(`✓ Generated ${routes.length} API routes:`);
  routes.forEach(r => console.log(`  ${r}`));

  return routes;
}

if (require.main === module) {
  generate();
}

module.exports = { generate };
