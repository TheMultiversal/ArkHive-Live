// ── Submit API ─────────────────────────────────────────────────

function generateSubmitRoute() {
  return `import { NextRequest, NextResponse } from 'next/server';

// Auto-generated by ArkHive Forge — ${new Date().toISOString()}
// Submit tips and evidence

export async function POST(request: NextRequest) {
  let body;
  try {
    body = await request.json();
  } catch {
    return NextResponse.json({ error: 'Invalid JSON body' }, { status: 400 });
  }

  const { subject, category, description, entities, sources, contact } = body;

  if (!subject || !description) {
    return NextResponse.json(
      { error: 'Subject and description are required' },
      { status: 400 }
    );
  }

  // Placeholder: Store submission securely
  const submission = {
    id: \`sub_\${Date.now()}\`,
    subject,
    category: category || 'general',
    description,
    entities: entities || '',
    sources: sources || '',
    contact: contact || '',
    submittedAt: new Date().toISOString(),
    status: 'pending',
  };

  return NextResponse.json({ success: true, submission }, { status: 201 });
}
`;
}

