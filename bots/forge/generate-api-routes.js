// ── Entity List API ────────────────────────────────────────────

function generateEntityListRoute() {
  return `import { NextRequest, NextResponse } from 'next/server';

// Auto-generated by ArkHive Forge — ${new Date().toISOString()}
// List entities by type with filtering, sorting, pagination

const VALID_TYPES = [${Object.values(schema.entities).map(e => `'${e.slug}'`).join(', ')}];

export async function GET(
  request: NextRequest,
  { params }: { params: Promise<{ type: string }> }
) {
  const { type } = await params;
  const { searchParams } = new URL(request.url);

  if (!VALID_TYPES.includes(type)) {
    return NextResponse.json(
      { error: \`Invalid entity type: \${type}. Valid types: \${VALID_TYPES.join(', ')}\` },
      { status: 400 }
    );
  }

  const sortBy = searchParams.get('sort') || 'name';
  const order = searchParams.get('order') || 'asc';
  const limit = Math.min(parseInt(searchParams.get('limit') || '50', 10), 500);
  const offset = parseInt(searchParams.get('offset') || '0', 10);
  const riskLevel = searchParams.get('riskLevel');
  const q = searchParams.get('q')?.toLowerCase();

  // Placeholder: Dynamic data loading from shard files
  return NextResponse.json({
    type,
    results: [],
    total: 0,
    limit,
    offset,
    sort: sortBy,
    order,
    filters: { riskLevel, q },
  });
}
`;
}

