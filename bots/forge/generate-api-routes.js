// ── Export API ──────────────────────────────────────────────────

function generateExportRoute() {
  return `import { NextRequest, NextResponse } from 'next/server';

// Auto-generated by ArkHive Forge — ${new Date().toISOString()}
// Export data as PDF, CSV, or JSON

const VALID_FORMATS = ['pdf', 'csv', 'json'];

export async function GET(
  request: NextRequest,
  { params }: { params: Promise<{ format: string }> }
) {
  const { format } = await params;
  const { searchParams } = new URL(request.url);

  if (!VALID_FORMATS.includes(format)) {
    return NextResponse.json(
      { error: \`Invalid format: \${format}. Valid: \${VALID_FORMATS.join(', ')}\` },
      { status: 400 }
    );
  }

  const type = searchParams.get('type') || 'all';
  const slug = searchParams.get('slug');

  if (format === 'json') {
    // Placeholder: Serialize requested data as JSON
    return NextResponse.json({
      format: 'json',
      type,
      slug,
      data: [],
      exportedAt: new Date().toISOString(),
    });
  }

  if (format === 'csv') {
    const csv = 'name,type,riskLevel\\n'; // Placeholder
    return new NextResponse(csv, {
      headers: {
        'Content-Type': 'text/csv',
        'Content-Disposition': \`attachment; filename="arkhive-export-\${Date.now()}.csv"\`,
      },
    });
  }

  // PDF would require a library like puppeteer or react-pdf
  return NextResponse.json({
    format: 'pdf',
    message: 'PDF export requires puppeteer or @react-pdf/renderer. Install and re-run forge.',
  });
}
`;
}

