#!/usr/bin/env node
// ═══════════════════════════════════════════════════════════════════
//  ARKHIVE FORGE — COMPONENT GENERATOR
//  Reads schema.js → generates React components for entity display,
//  detail views, cards, filters, forms, and section panels.
// ═══════════════════════════════════════════════════════════════════

const fs = require('fs');
const path = require('path');
const schema = require('./schema');

const SRC = path.resolve(__dirname, '../../src');
const COMPONENTS = path.join(SRC, 'components/generated');

function ensureDir(dir) {
  fs.mkdirSync(dir, { recursive: true });
}

// ── Utility ────────────────────────────────────────────────────

function capitalize(s) { return s.charAt(0).toUpperCase() + s.slice(1); }
function kebab(s) { return s.replace(/([a-z])([A-Z])/g, '$1-$2').toLowerCase(); }

// ── Card Component Generator ───────────────────────────────────

function generateEntityCard(entityName, def) {
  const name = `${entityName}Card`;
  const profileType = `${entityName}Profile`;
  const cardFields = Object.entries(def.fields)
    .filter(([, f]) => f.display === 'card')
    .map(([k]) => k);
  const badgeFields = Object.entries(def.fields)
    .filter(([, f]) => f.display === 'badge')
    .map(([k]) => k);

  return `'use client';
// Auto-generated by ArkHive Forge — ${new Date().toISOString()}
import Link from 'next/link';
import { ${profileType} } from '@/types/generated';

const riskColors: Record<string, string> = {
  critical: 'text-red-400 bg-red-950 border-red-800',
  extreme: 'text-red-400 bg-red-950 border-red-800',
  high: 'text-orange-400 bg-orange-950 border-orange-800',
  moderate: 'text-yellow-400 bg-yellow-950 border-yellow-800',
  medium: 'text-yellow-400 bg-yellow-950 border-yellow-800',
  low: 'text-green-400 bg-green-950 border-green-800',
};

interface ${name}Props {
  data: ${profileType};
  slug: string;
  href?: string;
}

export default function ${name}({ data, slug, href }: ${name}Props) {
  const riskLevel = data.riskLevel || 'low';
  const colorClass = riskColors[riskLevel] || riskColors.low;

  return (
    <Link href={href || \`/entities/${def.slug}/\${slug}\`} className="block group">
      <div className="glass-card p-4 transition-all duration-200 hover:border-red-700">
        <div className="flex items-start justify-between mb-2">
          <h3 className="text-zinc-100 font-semibold group-hover:text-red-400 transition-colors">
            {data.${cardFields[0] || 'name'}}
          </h3>
          <span className={\`text-xs px-2 py-0.5 border \${colorClass}\`}>
            {riskLevel.toUpperCase()}
          </span>
        </div>
${badgeFields.map(f => `        {data.${f} && (
          <span className="text-xs text-zinc-500 mb-1 block">{data.${f}}</span>
        )}`).join('\n')}
        <p className="text-zinc-400 text-sm line-clamp-3 mt-1">
          {data.description?.slice(0, 200) || ''}
        </p>
${def.relations?.controversies ? `        <div className="flex items-center gap-3 mt-3 text-xs text-zinc-500">
          {data.controversies && <span>{data.controversies.length} controversies</span>}
          {data.sources && <span>{data.sources.length} sources</span>}
        </div>` : ''}
      </div>
    </Link>
  );
}
`;
}

// ── Detail Section Component Generator ─────────────────────────

function generateSectionComponent(subTypeName, subTypeDef) {
  const name = `${subTypeName}Section`;
  const fields = Object.entries(subTypeDef.fields);

  return `'use client';
// Auto-generated by ArkHive Forge — ${new Date().toISOString()}
import { useState } from 'react';
import { motion, AnimatePresence } from 'framer-motion';
import { ChevronDown } from 'lucide-react';
import { ${subTypeName} } from '@/types/generated';

interface ${name}Props {
  items: ${subTypeName}[];
  title?: string;
}

export default function ${name}({ items, title = '${subTypeName.replace(/([A-Z])/g, ' $1').trim()}' }: ${name}Props) {
  const [expanded, setExpanded] = useState<Set<number>>(new Set());

  if (!items || items.length === 0) return null;

  const toggle = (i: number) => {
    setExpanded(prev => {
      const next = new Set(prev);
      next.has(i) ? next.delete(i) : next.add(i);
      return next;
    });
  };

  return (
    <div className="space-y-2">
      <h3 className="text-lg font-bold text-zinc-100 border-b border-red-900/30 pb-2">
        {title} <span className="text-sm text-zinc-500">({items.length})</span>
      </h3>
      {items.map((item, i) => (
        <div key={i} className="glass-card">
          <button
            onClick={() => toggle(i)}
            className="w-full flex items-center justify-between p-3 text-left"
          >
            <span className="text-zinc-200 font-medium">
              {${getDisplayField(fields)}}
            </span>
            <ChevronDown
              className={\`w-4 h-4 text-zinc-500 transition-transform \${expanded.has(i) ? 'rotate-180' : ''}\`}
            />
          </button>
          <AnimatePresence>
            {expanded.has(i) && (
              <motion.div
                initial={{ height: 0, opacity: 0 }}
                animate={{ height: 'auto', opacity: 1 }}
                exit={{ height: 0, opacity: 0 }}
                className="overflow-hidden"
              >
                <div className="px-3 pb-3 space-y-2">
${fields.filter(([k]) => !isDisplayField(k, fields)).map(([fieldName, fieldDef]) => {
  if (fieldDef.type === 'string[]') {
    return `                  {item.${fieldName} && item.${fieldName}.length > 0 && (
                    <div>
                      <span className="text-xs text-zinc-500 uppercase">${fieldName.replace(/([A-Z])/g, ' $1').trim()}</span>
                      <ul className="list-disc list-inside text-sm text-zinc-300">
                        {item.${fieldName}.map((s: string, j: number) => <li key={j}>{s}</li>)}
                      </ul>
                    </div>
                  )}`;
  }
  if (fieldDef.type === 'enum') {
    return `                  {item.${fieldName} && (
                    <span className="text-xs px-2 py-0.5 bg-zinc-800 text-zinc-300 border border-zinc-700">
                      {item.${fieldName}}
                    </span>
                  )}`;
  }
  if (fieldDef.type === 'boolean') {
    return `                  {item.${fieldName} !== undefined && (
                    <span className="text-xs text-zinc-400">${fieldName}: {item.${fieldName} ? 'Yes' : 'No'}</span>
                  )}`;
  }
  return `                  {item.${fieldName} && (
                    <p className="text-sm text-zinc-300">{item.${fieldName}}</p>
                  )}`;
}).join('\n')}
                </div>
              </motion.div>
            )}
          </AnimatePresence>
        </div>
      ))}
    </div>
  );
}
`;
}

function getDisplayField(fields) {
  // Pick the best display field: title > name > summary > description > first string
  const priorities = ['title', 'name', 'summary', 'description', 'label'];
  for (const p of priorities) {
    if (fields.find(([k]) => k === p)) return `item.${p}`;
  }
  const firstString = fields.find(([, v]) => v.type === 'string');
  return firstString ? `item.${firstString[0]}` : `String(i)`;
}

function isDisplayField(fieldName, fields) {
  const priorities = ['title', 'name', 'summary', 'description', 'label'];
  const displayField = priorities.find(p => fields.find(([k]) => k === p));
  return fieldName === displayField;
}

// ── Filter/Search Component ────────────────────────────────────

function generateFilterComponent(entityName, def) {
  const enumFields = Object.entries(def.fields)
    .filter(([, f]) => f.type === 'enum')
    .map(([k, f]) => ({ name: k, values: f.values }));

  return `'use client';
// Auto-generated by ArkHive Forge — ${new Date().toISOString()}
import { useState } from 'react';
import { Search, SlidersHorizontal } from 'lucide-react';

interface ${entityName}FilterProps {
  onFilter: (filters: Record<string, string>) => void;
  onSearch: (query: string) => void;
}

export default function ${entityName}Filter({ onFilter, onSearch }: ${entityName}FilterProps) {
  const [query, setQuery] = useState('');
  const [filters, setFilters] = useState<Record<string, string>>({});

  const handleSearch = (value: string) => {
    setQuery(value);
    onSearch(value);
  };

  const handleFilter = (key: string, value: string) => {
    const next = { ...filters, [key]: value };
    if (!value) delete next[key];
    setFilters(next);
    onFilter(next);
  };

  return (
    <div className="flex flex-wrap gap-3 items-center mb-6">
      <div className="relative flex-1 min-w-[200px]">
        <Search className="absolute left-3 top-1/2 -translate-y-1/2 w-4 h-4 text-zinc-500" />
        <input
          type="text"
          placeholder="Search ${entityName.toLowerCase()}s..."
          value={query}
          onChange={e => handleSearch(e.target.value)}
          className="w-full pl-10 pr-4 py-2 bg-zinc-900 border border-zinc-800 text-zinc-200 text-sm focus:border-red-700 focus:outline-none"
        />
      </div>
${enumFields.map(f => `      <select
        value={filters.${f.name} || ''}
        onChange={e => handleFilter('${f.name}', e.target.value)}
        className="bg-zinc-900 border border-zinc-800 text-zinc-300 text-sm px-3 py-2 focus:border-red-700 focus:outline-none"
      >
        <option value="">All ${capitalize(f.name)}s</option>
${f.values.map(v => `        <option value="${v}">${capitalize(v)}</option>`).join('\n')}
      </select>`).join('\n')}
    </div>
  );
}
`;
}

// ── Stats Bar Component ────────────────────────────────────────

function generateStatsBar(entityName, def) {
  const stats = [];
  for (const [k, v] of Object.entries(def.relations || {})) {
    if (v.type.endsWith('[]')) {
      stats.push({ key: k, label: k.replace(/([A-Z])/g, ' $1').trim() });
    }
  }

  return `'use client';
// Auto-generated by ArkHive Forge — ${new Date().toISOString()}
import { ${entityName}Profile } from '@/types/generated';

interface ${entityName}StatsProps {
  data: ${entityName}Profile;
}

export default function ${entityName}Stats({ data }: ${entityName}StatsProps) {
  const stats = [
${stats.map(s => `    { label: '${capitalize(s.label)}', value: data.${s.key}?.length || 0 },`).join('\n')}
  ];

  return (
    <div className="grid grid-cols-2 sm:grid-cols-3 lg:grid-cols-${Math.min(stats.length, 6)} gap-2 mb-6">
      {stats.map((s, i) => (
        <div key={i} className="glass-card p-3 text-center">
          <div className="text-2xl font-bold text-red-400">{s.value}</div>
          <div className="text-xs text-zinc-500 uppercase">{s.label}</div>
        </div>
      ))}
    </div>
  );
}
`;
}

// ── Master Generation ──────────────────────────────────────────

function generate() {
  ensureDir(COMPONENTS);

  let filesGenerated = 0;
  const manifest = [];

  // Generate entity cards
  for (const [name, def] of Object.entries(schema.entities)) {
    const content = generateEntityCard(name, def);
    const file = path.join(COMPONENTS, `${name}Card.tsx`);
    fs.writeFileSync(file, content, 'utf-8');
    manifest.push({ type: 'card', entity: name, file: `${name}Card.tsx` });
    filesGenerated++;
  }

  // Generate filter components
  for (const [name, def] of Object.entries(schema.entities)) {
    const content = generateFilterComponent(name, def);
    const file = path.join(COMPONENTS, `${name}Filter.tsx`);
    fs.writeFileSync(file, content, 'utf-8');
    manifest.push({ type: 'filter', entity: name, file: `${name}Filter.tsx` });
    filesGenerated++;
  }

  // Generate stats bars
  for (const [name, def] of Object.entries(schema.entities)) {
    if (def.relations) {
      const content = generateStatsBar(name, def);
      const file = path.join(COMPONENTS, `${name}Stats.tsx`);
      fs.writeFileSync(file, content, 'utf-8');
      manifest.push({ type: 'stats', entity: name, file: `${name}Stats.tsx` });
      filesGenerated++;
    }
  }

  // Generate sub-type section components
  for (const [name, def] of Object.entries(schema.subTypes)) {
    // Only generate sections for complex sub-types (not simple ones like TimelineEntry)
    const fieldCount = Object.keys(def.fields).length;
    if (fieldCount >= 3) {
      const content = generateSectionComponent(name, def);
      const file = path.join(COMPONENTS, `${name}Section.tsx`);
      fs.writeFileSync(file, content, 'utf-8');
      manifest.push({ type: 'section', subType: name, file: `${name}Section.tsx` });
      filesGenerated++;
    }
  }

  // Generate barrel export
  const barrel = manifest.map(m =>
    `export { default as ${path.basename(m.file, '.tsx')} } from './${path.basename(m.file, '.tsx')}';`
  ).join('\n');
  fs.writeFileSync(path.join(COMPONENTS, 'index.ts'), `// Auto-generated barrel export\n${barrel}\n`, 'utf-8');
  filesGenerated++;

  // Save manifest
  fs.writeFileSync(
    path.join(COMPONENTS, 'manifest.json'),
    JSON.stringify(manifest, null, 2),
    'utf-8'
  );

  console.log(`✓ Generated ${filesGenerated} component files in ${COMPONENTS}`);
  console.log(`  ${Object.keys(schema.entities).length} entity cards`);
  console.log(`  ${Object.keys(schema.entities).length} filter components`);
  console.log(`  ${Object.keys(schema.entities).length} stats bars`);
  console.log(`  ${manifest.filter(m => m.type === 'section').length} section panels`);

  return manifest;
}

if (require.main === module) {
  generate();
}

module.exports = { generate };
