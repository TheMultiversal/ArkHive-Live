#!/usr/bin/env node
/**
 * FIX SCRIPT: Fix the 3 data files that have entries misplaced after exports.
 * Moves all entries back inside the main record object.
 */
const fs = require('fs');
const path = require('path');

const ROOT = path.resolve(__dirname, '..');
const DATA_DIR = path.join(ROOT, 'src', 'data');

function fixDataFile(filePath, varName, typeName) {
  console.log(`\nFixing ${filePath}...`);
  let content = fs.readFileSync(filePath, 'utf8');
  
  // Strategy: extract all entry blocks, rebuild the file
  // Find the pattern: entries between the opening { and the first };
  
  // Find where "const varName: Record<...> = {" starts
  const recordStart = content.indexOf(`const ${varName}:`);
  if (recordStart === -1) {
    console.log(`  Could not find 'const ${varName}:', skipping`);
    return;
  }
  
  // Find the opening brace
  const openBrace = content.indexOf('{', recordStart);
  
  // Collect ALL entry blocks throughout the entire file
  // Pattern: '  'slug-name': {' 
  const entryRegex = /^  '([a-z0-9][a-z0-9-]*[a-z0-9])': \{$/gm;
  const entries = [];
  let match;
  let positions = [];
  
  while ((match = entryRegex.exec(content)) !== null) {
    positions.push({ slug: match[1], start: match.index });
  }
  
  // For each entry, find its closing '},\n'
  for (let i = 0; i < positions.length; i++) {
    const start = positions[i].start;
    const slug = positions[i].slug;
    
    // Find the end of this entry: look for '},\n' or '},\r\n' at indent level 2
    let depth = 0;
    let entryEnd = -1;
    let j = content.indexOf('{', start);
    
    for (let k = j; k < content.length; k++) {
      if (content[k] === '{') depth++;
      if (content[k] === '}') {
        depth--;
        if (depth === 0) {
          // Find the comma after this closing brace
          entryEnd = k + 1;
          if (content[k + 1] === ',') entryEnd = k + 2;
          break;
        }
      }
    }
    
    if (entryEnd > 0) {
      const entryText = content.substring(start, entryEnd);
      entries.push({ slug, text: entryText });
    }
  }
  
  console.log(`  Found ${entries.length} entries total`);
  
  // Deduplicate by slug (keep last occurrence = most recent)
  const uniqueEntries = new Map();
  for (const entry of entries) {
    uniqueEntries.set(entry.slug, entry.text);
  }
  console.log(`  ${uniqueEntries.size} unique entries after dedup`);
  
  // Get the import/type line
  const importLine = `import type { ${typeName} } from './types';`;
  
  // Rebuild the file
  const lines = [
    `// AUTO-GENERATED by ArkHive Migration — DO NOT EDIT MANUALLY`,
    importLine,
    ``,
    `const ${varName}: Record<string, ${typeName}> = {`,
  ];
  
  for (const [slug, text] of uniqueEntries) {
    lines.push(text);
  }
  
  lines.push(`};`);
  lines.push(``);
  lines.push(`export default ${varName};`);
  lines.push(`export type { ${typeName} } from './types';`);
  lines.push(``);
  
  fs.writeFileSync(filePath, lines.join('\n'), 'utf8');
  console.log(`  Written ${uniqueEntries.size} entries`);
}

fixDataFile(
  path.join(DATA_DIR, 'agencies', 'index.ts'),
  'agencies',
  'AgencyProfile'
);

fixDataFile(
  path.join(DATA_DIR, 'corporations', 'index.ts'),
  'corporations',
  'CorporationProfile'
);

fixDataFile(
  path.join(DATA_DIR, 'organizations', 'index.ts'),
  'organizations',
  'OrganizationProfile'
);

console.log('\nDone! All files rebuilt.');
