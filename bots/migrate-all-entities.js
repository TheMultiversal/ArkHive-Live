#!/usr/bin/env node
// ═══════════════════════════════════════════════════════════════════
//  ARKHIVE — FULL ENTITY MIGRATION
//  Extracts hardcoded data from page.tsx files into shard data files
//  for agencies, corporations, organizations, and investigations
// ═══════════════════════════════════════════════════════════════════

const fs = require('fs');
const path = require('path');

const ROOT = path.resolve(__dirname, '..');
const SRC = path.join(ROOT, 'src');

// ── Helpers ──────────────────────────────────────────────────────

function esc(s) {
  return (s || '').replace(/\\/g, '\\\\').replace(/'/g, "\\'").replace(/\n/g, '\\n');
}

function ensureDir(dir) {
  if (!fs.existsSync(dir)) fs.mkdirSync(dir, { recursive: true });
}

function backup(filePath) {
  if (!fs.existsSync(filePath)) return;
  const backupDir = path.join(__dirname, 'backups');
  ensureDir(backupDir);
  const name = path.basename(filePath).replace('.tsx', '');
  const dest = path.join(backupDir, `${name}.pre-migration.${Date.now()}.bak`);
  fs.copyFileSync(filePath, dest);
  console.log(`  Backed up → ${path.relative(ROOT, dest)}`);
}

// ── 1. AGENCIES MIGRATION ────────────────────────────────────────

function migrateAgencies() {
  console.log('\n═══ MIGRATING AGENCIES ═══');
  const pagePath = path.join(SRC, 'app', 'entities', 'agencies', 'page.tsx');
  backup(pagePath);

  const content = fs.readFileSync(pagePath, 'utf8');

  // Extract the agencies array using regex to find each object
  const agencies = [];
  const entryRegex = /\{\s*\n\s*id:\s*"([^"]+)",\s*\n\s*slug:\s*"([^"]+)",\s*\n\s*name:\s*"([^"]+)",\s*\n\s*type:\s*"agency",\s*\n\s*description:\s*"((?:[^"\\]|\\.)*)"\s*,\s*\n\s*role:\s*"([^"]+)",\s*\n\s*investigationCount:\s*(\d+),\s*\n\s*riskLevel:\s*"([^"]+)",/g;

  let match;
  while ((match = entryRegex.exec(content)) !== null) {
    agencies.push({
      id: match[1],
      slug: match[2],
      name: match[3],
      description: match[4].replace(/\\"/g, '"'),
      role: match[5],
      investigationCount: parseInt(match[6]),
      riskLevel: match[7],
    });
  }

  console.log(`  Extracted ${agencies.length} agencies`);

  if (agencies.length === 0) {
    console.log('  WARNING: No agencies extracted! Check regex.');
    return 0;
  }

  // Write data file
  const dataDir = path.join(SRC, 'data', 'agencies');
  ensureDir(dataDir);

  const lines = [`// AUTO-GENERATED by ArkHive Migration — DO NOT EDIT MANUALLY`,
    `import type { AgencyProfile } from './types';`,
    ``,
    `const agencies: Record<string, AgencyProfile> = {`];

  for (const a of agencies) {
    lines.push(`  '${a.slug}': {`);
    lines.push(`    id: '${esc(a.id)}',`);
    lines.push(`    slug: '${esc(a.slug)}',`);
    lines.push(`    name: '${esc(a.name)}',`);
    lines.push(`    type: 'agency',`);
    lines.push(`    description: '${esc(a.description)}',`);
    lines.push(`    role: '${esc(a.role)}',`);
    lines.push(`    investigationCount: ${a.investigationCount},`);
    lines.push(`    riskLevel: '${a.riskLevel}',`);
    lines.push(`  },`);
  }

  lines.push(`};`);
  lines.push(``);
  lines.push(`export default agencies;`);
  lines.push(`export type { AgencyProfile };`);
  lines.push(``);

  fs.writeFileSync(path.join(dataDir, 'index.ts'), lines.join('\n'), 'utf8');
  console.log(`  Wrote src/data/agencies/index.ts (${agencies.length} agencies)`);
  return agencies.length;
}

