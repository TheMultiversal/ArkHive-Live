// ── 3. ORGANIZATIONS MIGRATION ───────────────────────────────────

function migrateOrganizations() {
  console.log('\n═══ MIGRATING ORGANIZATIONS ═══');
  const pagePath = path.join(SRC, 'app', 'entities', 'organizations', 'page.tsx');
  backup(pagePath);

  const content = fs.readFileSync(pagePath, 'utf8');

  const orgs = [];

  // Organizations have two formats:
  // Format 1 (original): { name: 'X', slug: 'x', type: 'Y', description: 'Z', riskLevel: 'critical' as const, members: 'N' }
  // Format 2 (bot-injected): { id: "x-ep", slug: "x", name: "X", type: "organization", description: "Z...", riskLevel: 'high' as const, members: 'N/A' }

  // Format 1: name first (single quotes)
  const f1 = /\{\s*\n\s*name:\s*'([^']+)',\s*\n\s*slug:\s*'([^']+)',\s*\n\s*type:\s*'([^']+)',\s*\n\s*description:\s*'([^']*(?:\\.[^']*)*)',\s*\n\s*riskLevel:\s*'([^']+)'\s*as const,\s*\n\s*members:\s*'([^']*)'/g;

  let match;
  while ((match = f1.exec(content)) !== null) {
    orgs.push({
      name: match[1],
      slug: match[2],
      type: match[3],
      description: match[4],
      riskLevel: match[5],
      members: match[6],
    });
  }

  // Format 2: id first (double quotes, bot-injected)
  const f2 = /\{\s*\n\s*id:\s*"([^"]+)",\s*\n\s*slug:\s*"([^"]+)",\s*\n\s*name:\s*"([^"]+)",\s*\n\s*type:\s*"([^"]+)",\s*\n\s*description:\s*"((?:[^"\\]|\\.)*)"\s*,\s*\n\s*riskLevel:\s*'([^']+)'\s*as const,\s*\n\s*members:\s*'([^']*)'/g;

  while ((match = f2.exec(content)) !== null) {
    // Avoid duplicates by slug
    if (!orgs.find(o => o.slug === match[2])) {
      orgs.push({
        name: match[3],
        slug: match[2],
        type: match[4],
        description: match[5].replace(/\\"/g, '"'),
        riskLevel: match[6],
        members: match[7],
        id: match[1],
      });
    }
  }

  console.log(`  Extracted ${orgs.length} organizations`);

  if (orgs.length === 0) {
    console.log('  WARNING: No organizations extracted! Check regex.');
    return 0;
  }

  // Deduplicate by slug (keep first occurrence)
  const seen = new Set();
  const unique = [];
  for (const o of orgs) {
    if (!seen.has(o.slug)) {
      seen.add(o.slug);
      unique.push(o);
    }
  }
  console.log(`  ${unique.length} unique organizations (removed ${orgs.length - unique.length} duplicates)`);

  const dataDir = path.join(SRC, 'data', 'organizations');
  ensureDir(dataDir);

  const lines = [`// AUTO-GENERATED by ArkHive Migration — DO NOT EDIT MANUALLY`,
    `import type { OrganizationProfile } from './types';`,
    ``,
    `const organizations: Record<string, OrganizationProfile> = {`];

  for (const o of unique) {
    lines.push(`  '${esc(o.slug)}': {`);
    lines.push(`    name: '${esc(o.name)}',`);
    lines.push(`    slug: '${esc(o.slug)}',`);
    lines.push(`    type: '${esc(o.type)}',`);
    lines.push(`    description: '${esc(o.description)}',`);
    lines.push(`    riskLevel: '${esc(o.riskLevel)}',`);
    lines.push(`    members: '${esc(o.members)}',`);
    if (o.id) lines.push(`    id: '${esc(o.id)}',`);
    lines.push(`  },`);
  }

  lines.push(`};`);
  lines.push(``);
  lines.push(`export default organizations;`);
  lines.push(`export type { OrganizationProfile };`);
  lines.push(``);

  fs.writeFileSync(path.join(dataDir, 'index.ts'), lines.join('\n'), 'utf8');
  console.log(`  Wrote src/data/organizations/index.ts (${unique.length} organizations)`);
  return unique.length;
}

// ── 4. INVESTIGATIONS MIGRATION ──────────────────────────────────

function migrateInvestigations() {
  console.log('\n═══ MIGRATING INVESTIGATIONS ═══');
  const pagePath = path.join(SRC, 'app', 'investigations', '[slug]', 'page.tsx');
  backup(pagePath);

  const content = fs.readFileSync(pagePath, 'utf8');

  // Find the investigationDatabase object
  const dbStart = content.indexOf('const investigationDatabase');
  if (dbStart === -1) {
    console.log('  ERROR: Could not find investigationDatabase');
    return 0;
  }

  // We need to extract the Record<string, InvestigationData> entries
  // Strategy: find each top-level key in the object literal
  const keyRegex = /\n\s*'([a-z0-9-]+)'\s*:\s*\{/g;

  const slugs = [];
  let km;
  // Only search within the database portion (from dbStart)
  const dbContent = content.substring(dbStart);
  while ((km = keyRegex.exec(dbContent)) !== null) {
    slugs.push({ slug: km[1], offset: km.index + dbStart });
  }

  console.log(`  Found ${slugs.length} investigation slugs`);

  if (slugs.length === 0) {
    console.log('  WARNING: No investigations found!');
    return 0;
  }

  // Extract each investigation block by brace-matching
  const investigations = [];
  for (let i = 0; i < slugs.length; i++) {
    const slug = slugs[i].slug;
    const blockSearch = `'${slug}': {`;
    const startIdx = content.indexOf(blockSearch, slugs[i].offset > 100 ? slugs[i].offset - 100 : 0);
    if (startIdx === -1) continue;

    const braceStart = content.indexOf('{', startIdx + blockSearch.length - 1);
    let depth = 0;
    let endIdx = -1;
    for (let j = braceStart; j < content.length; j++) {
      if (content[j] === '{') depth++;
      if (content[j] === '}') {
        depth--;
        if (depth === 0) {
          endIdx = j + 1;
          break;
        }
      }
    }

    if (endIdx === -1) continue;

    // Get the raw block
    const block = content.substring(braceStart, endIdx);
    investigations.push({ slug, block });
  }

  console.log(`  Extracted ${investigations.length} investigation blocks`);

  // Write to shard files by first letter
  const dataDir = path.join(SRC, 'data', 'investigations');
  ensureDir(dataDir);

  // Group by first letter
  const shards = {};
  for (const inv of investigations) {
    const letter = inv.slug[0].toLowerCase();
    if (!shards[letter]) shards[letter] = [];
    shards[letter].push(inv);
  }

  const shardLetters = Object.keys(shards).sort();

  for (const letter of shardLetters) {
    const items = shards[letter];
    const lines = [
      `// AUTO-GENERATED by ArkHive Migration — DO NOT EDIT MANUALLY`,
      `import type { InvestigationData } from './types';`,
      ``,
      `const investigations_${letter}: Record<string, InvestigationData> = {`,
    ];

    for (const { slug, block } of items) {
      lines.push(`  '${slug}': ${block},`);
    }

    lines.push(`};`);
    lines.push(``);
    lines.push(`export default investigations_${letter};`);
    lines.push(``);

    fs.writeFileSync(path.join(dataDir, `${letter}.ts`), lines.join('\n'), 'utf8');
  }

  console.log(`  Wrote ${shardLetters.length} shard files: ${shardLetters.join(', ')}`);

  // Write barrel index
  const indexLines = [
    `// AUTO-GENERATED by ArkHive Migration — DO NOT EDIT MANUALLY`,
    `import type { InvestigationData } from './types';`,
    ``,
  ];

  for (const letter of shardLetters) {
    indexLines.push(`import shard_${letter} from './${letter}';`);
  }

  indexLines.push(``);
  indexLines.push(`const investigationDatabase: Record<string, InvestigationData> = {`);
  for (const letter of shardLetters) {
    indexLines.push(`  ...shard_${letter},`);
  }
  indexLines.push(`};`);
  indexLines.push(``);
  indexLines.push(`export default investigationDatabase;`);
  indexLines.push(`export type { InvestigationData, InvestigationAffiliation, InvestigationSource } from './types';`);
  indexLines.push(``);

  fs.writeFileSync(path.join(dataDir, 'index.ts'), indexLines.join('\n'), 'utf8');
  console.log(`  Wrote src/data/investigations/index.ts (barrel export)`);

  return investigations.length;
}

// ── MAIN ─────────────────────────────────────────────────────────

